import { Injectable, ExecutionContext } from '@nestjs/common';
import { Reflector } from '@nestjs/core';
import { AuthGuard } from '@nestjs/passport';

@Injectable()
export class JwtAuthGuard extends AuthGuard('jwt') {
  constructor(private reflector: Reflector) {
    super();
  }

  canActivate(context: ExecutionContext): boolean | Promise<boolean> {
    // Check if the endpoint is marked as public
    const isPublic = this.reflector.getAllAndOverride<boolean>('isPublic', [
      context.getHandler(),
      context.getClass(),
    ]);

    if (isPublic) {
      return true; // Skip JWT authentication for public endpoints
    }

    const canActivateResult = super.canActivate(context);
    if (typeof canActivateResult === 'boolean' || canActivateResult instanceof Promise) {
      return canActivateResult;
    }
    throw new Error('Unexpected return type from canActivate');
  }
}
